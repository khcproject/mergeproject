package dto;

public class StarsDTO {

	public StarsDTO() {
	}

	private int p_num;
	private String id;
	private float s_stars;

	public int getP_num() {
		return p_num;
	}

	public void setP_num(int p_num) {
		this.p_num = p_num;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public float getS_stars() {
		return s_stars;
	}

	public void setS_stars(float s_stars) {
		this.s_stars = s_stars;
	}

	

}
