package dto;

public class TalkDTO {

	public TalkDTO() {
	}
	
	private int t_num;
	private String id;
	private String t_resp;
	private String t_message;
	private String t_date;
	private String t_check;
	private String t_title;
	
	
	public int getT_num() {
		return t_num;
	}
	public void setT_num(int t_num) {
		this.t_num = t_num;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getT_resp() {
		return t_resp;
	}
	public void setT_resp(String t_resp) {
		this.t_resp = t_resp;
	}
	public String getT_message() {
		return t_message;
	}
	public void setT_message(String t_message) {
		this.t_message = t_message;
	}
	public String getT_date() {
		return t_date;
	}
	public void setT_date(String t_date) {
		this.t_date = t_date;
	}
	public String getT_check() {
		return t_check;
	}
	public void setT_check(String t_check) {
		this.t_check = t_check;
	}
	public String getT_title() {
		return t_title;
	}
	public void setT_title(String t_title) {
		this.t_title = t_title;
	}
	
	
	
	
}
